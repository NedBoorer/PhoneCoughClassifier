def get_text(key: str, language: str = "en") -> str:
    """
    Get translated text for a key.
    Falls back to English if translation not found.
    """
    translations = TRANSLATIONS.get(key, {})
    return translations.get(language, translations.get("en", key))


def get_class_name(classification: str, language: str = "en") -> str:
    """Get translated classification name"""
    class_key = f"class_{classification}"
    return get_text(class_key, language)


def get_language_config(code: str) -> Optional[LanguageConfig]:
    """Get language configuration by code"""
    return LANGUAGES.get(code)


