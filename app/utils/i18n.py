def get_text(key: str, language: str = "en") -> str:
    """
    Get translated text for a key.
    Falls back to English if translation not found.
    """
    translations = TRANSLATIONS.get(key, {})
    return translations.get(language, translations.get("en", key))


def get_class_name(classification: str, language: str = "en") -> str:
    """Get translated classification name"""
    class_key = f"class_{classification}"
    return get_text(class_key, language)


def get_language_config(code: str) -> Optional[LanguageConfig]:
    """Get language configuration by code"""
    return LANGUAGES.get(code)


def format_sms_multilingual(
    classification: str,
    confidence: float,
    recommendation: str,
    language: str = "en"
) -> str:
    """Format SMS message in specified language"""
    class_name = get_class_name(classification, language)
    confidence_pct = int(confidence * 100)
    helpline = get_text("helpline", language)
    
    # Get localized strings (fallback to English if missing)
    title = get_text("result_title", language)
    lbl_class = get_text("label_classification", language)
    lbl_conf = get_text("label_confidence", language)
    disclaimer = get_text("disclaimer", language)
    
    return f"""{title}

{lbl_class}: {class_name}
{lbl_conf}: {confidence_pct}%

{recommendation}

{disclaimer}
{helpline}"""
